public class Swap {
    public static void main(String args[]){
        String first_name = "Jhon";
        String last_name = "Singh";
        String temp;
        temp = first_name;
        first_name = last_name;
        last_name = temp;
        System.out.println("My first name is : "+ first_name);
        System.out.println("My last name is : "+ last_name);

    }
}
